package com.insframework.system.curruser;

import java.util.List;
import java.util.Map;


/**
 * 当前用户
 * @author guom
 *
 */
public class CurrentUser {
	
	private String name;								//用户姓名
	private String id;									//用户id
	private String account;								//用户账号
	private String organizationId;						//部门id
	private String organizationName;					//部门名称
	private String companyId;							//公司id
	private String companyName;							//公司名称
	private List<String> roleIds;						//角色id集合
	private List<Map<String, Object>> resources;		//可访问资源集合	
	private boolean isAdmin;							//当前用户是否是超级管理员
	private Map<String, Object> operateMap;				//当前用户的操作map
	private int bizDataLevel;							//当前用户的数据权限级别	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getAccount() {
		return account;
	}
	public void setAccount(String account) {
		this.account = account;
	}
	public String getOrganizationId() {
		return organizationId;
	}
	public void setOrganizationId(String organizationId) {
		this.organizationId = organizationId;
	}
	public String getOrganizationName() {
		return organizationName;
	}
	public void setOrganizationName(String organizationName) {
		this.organizationName = organizationName;
	}
	public List<String> getRoleIds() {
		return roleIds;
	}
	public void setRoleIds(List<String> roleIds) {
		this.roleIds = roleIds;
	}
	public List<Map<String, Object>> getResources() {
		return resources;
	}
	public void setResources(List<Map<String, Object>> resources) {
		this.resources = resources;
	}
	
	public boolean getIsAdmin() {
		return isAdmin;
	}
	public void setIsAdmin(boolean isAdmin) {
		this.isAdmin = isAdmin;
	}
	public Map<String, Object> getOperateMap() {
		return operateMap;
	}
	public void setOperateMap(Map<String, Object> operateMap) {
		this.operateMap = operateMap;
	}
	public int getBizDataLevel() {
		return bizDataLevel;
	}
	public void setBizDataLevel(int bizDataLevel) {
		this.bizDataLevel = bizDataLevel;
	}
	public String getCompanyId() {
		return companyId;
	}
	public void setCompanyId(String companyId) {
		this.companyId = companyId;
	}
	public String getCompanyName() {
		return companyName;
	}
	public void setCompanyName(String companyName) {
		this.companyName = companyName;
	}
	
}
